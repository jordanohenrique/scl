package br.com.scl.PO;

public final class PORota {

	private int RotId;
    private String RotCodigo;
    private String RotNome;
    
	public int getRotId() {
		return RotId;
	}
	public void setRotId(int rotId) {
		RotId = rotId;
	}
	public String getRotCodigo() {
		return RotCodigo;
	}
	public void setRotCodigo(String rotCodigo) {
		RotCodigo = rotCodigo;
	}
	public String getRotNome() {
		return RotNome;
	}
	public void setRotNome(String rotNome) {
		RotNome = rotNome;
	}
	
    
}
