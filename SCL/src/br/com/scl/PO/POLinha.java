package br.com.scl.PO;

public final class POLinha {

    private int LinId;
    private String LinCodigo;
    private String LinNome;
    private int RotId;
	public int getLinId() {
		return LinId;
	}
	public void setLinId(int linId) {
		LinId = linId;
	}
	public String getLinCodigo() {
		return LinCodigo;
	}
	public void setLinCodigo(String linCodigo) {
		LinCodigo = linCodigo;
	}
	public String getLinNome() {
		return LinNome;
	}
	public void setLinNome(String linNome) {
		LinNome = linNome;
	}
	public int getRotId() {
		return RotId;
	}
	public void setRotId(int rotId) {
		RotId = rotId;
	}
    
    
}
